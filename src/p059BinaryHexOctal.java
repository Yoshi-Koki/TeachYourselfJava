
public class p059BinaryHexOctal {
	public static void main(String args[]) {
		int i = 11;
		System.out.println("Binary is " + Integer.toBinaryString(i)); // 2進数
		System.out.println("Hex is " + Integer.toHexString(i)); // 16進数
		System.out.println("Octal is " + Integer.toOctalString(i)); // 8進数
	}

}
